/***************************************************************************
* 函 数 名: DRI_CLK_GetClockFreq
* 功能描述：获取各总线的时钟频值函数
* 入口参数：
            ClkFreqValue *pstcClkFreq：时钟频率值结构体指针
* 出口参数：
            无
* 注意事项:  
            无
* 例    如:
* 修改记录: 
*           2025-08-29 BY:YJX
***************************************************************************/
void DRI_CLK_GetClockFreq(ClkFreqValue *pstcClkFreq)
{     
     pstcClkFreq->hclkFreq = rcu_clock_freq_get(CK_AHB);
     pstcClkFreq->SysTickFreq = rcu_clock_freq_get(CK_AHB) / 8;
     pstcClkFreq->pclk1Freq = rcu_clock_freq_get(CK_APB1);
     pstcClkFreq->pclk2Freq = rcu_clock_freq_get(CK_APB2);
}

