/***************************************************************************
* 函 数 名: DRI_RCU_GetResetFlag
* 功能描述：获取复位类型
* 入口参数：
            无
* 出口参数：
            复位类型
* 注意事项:  
            无
* 例     如:
* 修改记录 :
*           2025-09-08 BY:YJX
***************************************************************************/
ResetFlag DRI_RCU_GetResetFlag(void)
{
     u32 u32resetFlag = RCU_RSTSCK;

     if(u32resetFlag & RCU_RSTSCK_LPRSTF)
     {
          return LP_Rst;
     }
     else if(u32resetFlag & RCU_RSTSCK_WWDGTRSTF)
     {
          return WWDGT_Rst;
     }
     else if(u32resetFlag & RCU_RSTSCK_FWDGTRSTF)
     {
          return FWDGT_Rst;
     }
     else if(u32resetFlag & RCU_RSTSCK_SWRSTF)
     {
          return SW_Rst;
     }
     else if(u32resetFlag & RCU_RSTSCK_PORRSTF)
     {
          return POR_Rst;
     }
     else if(u32resetFlag & RCU_RSTSCK_EPRSTF)
     {
          return EP_Rst;
     }
     else if(u32resetFlag & RCU_RSTSCK_BORRSTF)
     {
          return BOR_Rst;
     }          
     else
     {
          return Multiply_Rst;
     }
}

